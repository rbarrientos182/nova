    private void cargar(String valor) {
        try{
            String [] titulos={"ID","Usuario","Email","Estatus"};
            String [] registros= new String[5];
            model =new DefaultTableModel(null,titulos);
            
            String cons="SELECT idusuario,username,email,IF(estatus,'Activo','Inactivo') AS estatusdes FROM usuario WHERE CONCAT(username,'',email) LIKE '%"+valor+"%'";
            Statement st= cn.createStatement();
            ResultSet rs = st.executeQuery(cons);
            while(rs.next()){
                registros[0]=rs.getString("idusuario");
                registros[1]=rs.getString("username");
                registros[2]=rs.getString("email");
                registros[3]=rs.getString("estatusdes");
                model.addRow(registros);      
            }
            tbUsuarios.setModel(model);
            tbUsuarios.getColumnModel().getColumn(0).setPreferredWidth(50);
            tbUsuarios.getColumnModel().getColumn(1).setPreferredWidth(100);
            tbUsuarios.getColumnModel().getColumn(2).setPreferredWidth(100);
            tbUsuarios.getColumnModel().getColumn(3).setPreferredWidth(100);
            }catch(Exception e){
                System.out.println(e.getMessage());
                 }
     
    }
