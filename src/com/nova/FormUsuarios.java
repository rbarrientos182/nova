    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Herramientas tool = new Herramientas();
        String user;
        String email;
        String pass;
        String sql;
        user=txtUser.getText();
        email=txtCorreo.getText();
        pass= txtPass.getText();
        if(!"".equals(user) && !"".equals(email) && !"".equals(pass)){
            //llamamos al metodo validarEmail
            if(tool.validarEmail(email)){
                if(tool.validarPass(pass)){
                    sql="INSERT INTO usuario (username,email,password,estatus) VALUES (?,?,?,?)";
                    try {
                        PreparedStatement pst  = cn.prepareStatement(sql);
                        pst.setString(1, user);
                        pst.setString(2, email);
                        pst.setString(3, pass);
                        pst.setInt(4, selectEstatus.getSelectedIndex());
                        int n=pst.executeUpdate();
                        if(n>0){
                        JOptionPane.showMessageDialog(null, "Registro Guardado con Éxito");
                        bloquear();
                        }
                        cargar("");
                    } catch (SQLException ex) {
                        Logger.getLogger(FormUsuarios.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "La contraseña debe de contener un mínimo de 5 caracteres", "Aviso", WARNING_MESSAGE);
                }
                
            }
            else{
                JOptionPane.showMessageDialog(null, "El correo no es válido", "Aviso", WARNING_MESSAGE);
            }
        }
        else{
           JOptionPane.showMessageDialog(null, "Los campos necesitan contener información"); 
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

